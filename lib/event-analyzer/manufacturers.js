export function getManufacturersNames(mfrId) {
	console.assert(Number.isInteger(mfrId) || mfrId?.length === 3);

	const mfrIdStr = bytesToHex((Array.isArray(mfrId)) ? mfrId : [mfrId]);
	const names = manufacturers[mfrIdStr];
	if (!names) {
		return null;
	}
	console.assert(names.longName, '"longName" is always required.');

	return {
		mfrName: names.shortName ?? names.longName,
		mfrFullName: names.longName,
	};
}
